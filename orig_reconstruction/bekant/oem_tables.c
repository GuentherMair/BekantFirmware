/**
 * @file oem_tables.c
 * @brief Recovered data tables from the OEM firmware (0x1046-0x10D7).
 *
 * The OEM firmware stores a 141-byte lookup table in flash as a `retlw`
 * dispatch: a function at 0x1046 takes a 1-byte index in WREG,
 * subtracts 1, and uses the result to compute a PCLATH offset for
 * a series of `retlw <byte>` instructions. Each `retlw` returns one
 * byte of the table to the caller.
 *
 * Structurally the table is divided into three sections:
 *
 *   1. Header (0x104B-0x105F, 21 bytes) — version / magic / size
 *      markers; the `0x80` / `0x81` / `0xc1` patterns are
 *      sentinel values that delimit records.
 *
 *   2. ASCII IDs (0x1069-0x1080, 24 bytes) — the strings
 *      "ER\1\0", "A\2\1\1\0\0\0\0", "ER\1\0", "F\3\9\9\0\0\0\0"
 *      are used to identify the table type and which EEPROM page
 *      follows.
 *
 *   3. EEPROM image (0x1081-0x10D7, 87 bytes) — the actual
 *      byte-by-byte image of the OEM defaults that the firmware
 *      writes to EEPROM on first boot. The pattern
 *      `0x50 0x00 0x80 0x03 0x32 0x00` is the EEPROM-write
 *      unlock sequence (EECON2 = 0x55, 0xAA) plus a single
 *      byte value 0x32 written to EEDATA.
 *
 *   4. Tail records (0x10CC-0x10D7) — the 4-byte records
 *      `0xc1 0x00 0x80 0x03` and `0x32 0x00 0x92 0x00` and
 *      `0x80 0x03 0xf4 0x01` are checksum / end-of-table
 *      markers.
 *
 * The exact semantics of each record are not fully recovered —
 * this file is a **byte-for-byte** transcription of the table,
 * not a decoded C struct.
 */

#include "oem_tables.h"

/* The table starts at 0x104B (after the 5-word function preamble
 * at 0x1046-0x104A) and ends at 0x10D7. That is 141 bytes. */
const uint8_t oem_retlw_table[141] = {
    /* 0x104B - header (21 bytes) */
    0x00, 0x2c, 0x38, 0x80, 0x81, 0x08, 0xc1, 0x80, 0x03, 0x50, 0x80, 0x03,
    0x11, 0x81, 0x03, 0x92, 0x81, 0x03, 0x80, 0x00, 0x80,

    /* 0x1060 - sentinel pattern (8 bytes) */
    0x08, 0xc8, 0x00, 0x80, 0x00, 0x80, 0x08, 0xc8,

    /* 0x1068 - sentinel (1 byte) */
    0x00,

    /* 0x1069 - ASCII "ER" + record (8 bytes) */
    0x45, 0x52, 0x01, 0x00, 0x41, 0x02, 0x01, 0x01,

    /* 0x1071 - zero pad (4 bytes) */
    0x00, 0x00, 0x00, 0x00,

    /* 0x1075 - ASCII "ER" + record (8 bytes) */
    0x45, 0x52, 0x01, 0x00, 0x46, 0x03, 0x09, 0x09,

    /* 0x107D - zero pad (4 bytes) */
    0x00, 0x00, 0x00, 0x00,

    /* 0x1081 - record (3 bytes) */
    0x01, 0x00, 0x11,

    /* 0x1084 - 2-byte record (2 bytes) */
    0x02, 0x00,

    /* 0x1086 - 4-byte record (4 bytes) */
    0x50, 0x03, 0x00, 0x92,

    /* 0x108A - 2-byte record (2 bytes) */
    0x04, 0x00,

    /* 0x108C - 4-byte record (4 bytes) */
    0x11, 0x05, 0x00, 0xc1,

    /* 0x1090 - 2-byte record (2 bytes) */
    0x7d, 0x00,

    /* 0x1092 - 3-byte record (3 bytes) */
    0x00, 0x08, 0xe8,

    /* 0x1095 - 3-byte record (3 bytes) */
    0x03, 0x11, 0x00,

    /* 0x1098 - 4-byte record (4 bytes) */
    0x80, 0x03, 0x32, 0x00,

    /* 0x109C - 6-byte record repeated 12 times (72 bytes) */
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,
    0x50, 0x00, 0x80, 0x03, 0x32, 0x00,

    /* 0x10CC - tail record (4 bytes) */
    0xc1, 0x00, 0x80, 0x03,

    /* 0x10D0 - 4-byte record (4 bytes) */
    0x32, 0x00, 0x92, 0x00,

    /* 0x10D4 - 4-byte record (4 bytes) */
    0x80, 0x03, 0xf4, 0x01,
};

/* Sanity: this should be 141 bytes. */
_Static_assert(sizeof(oem_retlw_table) == 141,
               "oem_retlw_table must be exactly 141 bytes");
